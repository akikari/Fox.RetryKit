//==================================================================================================
// Defines the delay strategy for retry operations.
// Used to determine how delays between retries are calculated.
//==================================================================================================

namespace Fox.RetryKit;

//==================================================================================================
/// <summary>
/// Defines the delay strategy for retry operations.
/// </summary>
//==================================================================================================
public enum DelayStrategy
{
    //==============================================================================================
    /// <summary>
    /// Fixed delay between retry attempts.
    /// </summary>
    //==============================================================================================
    Fixed,

    //==============================================================================================
    /// <summary>
    /// Exponentially increasing delay between retry attempts.
    /// </summary>
    //==============================================================================================
    Exponential
}
